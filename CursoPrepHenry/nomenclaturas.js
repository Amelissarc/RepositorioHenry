/* ¿Qué es una nomenclatura?

Las nomenclaturas son formas de llamar a las cosas. 
En programación existen ciertas reglas sobre las cuales llevamos uniformidad en el código 
y nos proporcionan un estándar de trabajo especialmente dentro de equipos de desarrollo de software.

Existen 3 tipos de nomenclatura principal: camelCase, PascalCase, snake_case. */

/* camelCase --> Este modo de nombrar implica la unión de dos o más palabras sin espacios entre ellas,
pero diferenciadas por una letra mayúscula inicial a partir de la segunda palabra, 
por ejemplo: holaMundo / funcionEdadAños. */

/* PascalCase --> Este modo de nombrar implica la unión de dos o más palabras sin espacios entre ellas, 
pero diferenciadas por una letra mayúscula inicial a partir de la segunda palabra, 
por ejemplo: holaMundo / funcionEdadAños. */

/* snake_case --> Este modo de nombrar implica la unión de dos o más palabras sin espacios entre ellas, 
pero diferenciadas por una letra mayúscula inicial a partir de la segunda palabra, 
por ejemplo: holaMundo / funcionEdadAños. */